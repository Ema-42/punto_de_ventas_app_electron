<template>
  <div>
    <div class="bg-gray-100 p-4 rounded-lg shadow-md mb-4">
      <div class="flex justify-between items-center">
        <h1 class="text-2xl font-bold text-gray-700 flex items-center">
          Productos
          <img :src="cubiertos" alt="Cubierto" class="w-5 h-5 ml-2 filter brightness-0 dark:brightness-80" />
        </h1>

        <div>
          <div class="flex gap-2 items-center">
            <!-- Contenedor del input con icono -->
            <div class="relative w-64">
              <input
                v-model="searchQuery"
                type="text"
                placeholder="Buscar producto..."
                class="border p-2 rounded-lg shadow-sm w-full focus:ring-2 focus:ring-red-400 outline-none pl-10 bg-white"
              />
              <img :src="buscar" :alt="buscar.text" class="w-5 h-5 absolute left-3 top-1/2 transform -translate-y-1/2 text-red-600" />
            </div>

            <!-- Bot√≥n de agregar producto con icono -->
            <button
              @click="abrirModal"
              class="bg-red-600 text-white px-4 py-2 rounded-lg shadow-md hover:bg-red-700 transition flex items-center gap-2"
            >
              Agregar Producto
              <img :src="agregar" :alt="agregar.text" class="w-5 h-5 opacity-80" />
            </button>

            <ProductosCrear :mostrarModal="mostrarModal" @cerrar="cerrarModal" />
          </div>
        </div>
      </div>
    </div>

    <!-- Tabla estilizada -->
    <div class="overflow-x-auto bg-white shadow-lg rounded-lg">
      <table class="w-full border-collapse">
        <thead>
          <tr class="bg-gray-400 text-white">
            <th class="p-3 text-left">ID</th>
            <th class="p-3 text-left">Nombre</th>
            <th class="p-3 text-left">Precio</th>
            <th class="p-3 text-left">Imagen</th>
            <th class="p-3 text-left">Categor√≠a</th>
            <th class="p-3 text-left">Stock</th>
            <th class="p-3 text-center">Acciones</th>
          </tr>
        </thead>
        <tbody>
          <tr v-for="producto in paginatedProductos" :key="producto.id" class="border-b hover:bg-gray-100 transition">
            <td class="p-3">{{ producto.id }}</td>
            <td class="p-3">{{ producto.nombre }}</td>
            <td class="p-3 font-semibold text-green-600">${{ producto.precio }}</td>
            <td class="p-3">
              <img v-if="producto.imagen_url" :src="producto.imagen_url" class="w-12 h-12 rounded-full mx-auto shadow-md" />
              <span v-else class="text-gray-500">Sin imagen</span>
            </td>
            <td class="p-3">{{ producto.categoria?.nombre || "Sin categor√≠a" }}</td>
            <td class="p-3">
              <span :class="getStockClass(producto)">
                {{ producto.maneja_stock ? `${producto.stock} unidades` : "No aplica" }}
              </span>
            </td>
            <td class="p-3 flex justify-center space-x-3">
              <button class="text-blue-500 hover:text-blue-700 transition">
                ‚úèÔ∏è
              </button>
              <button class="text-red-500 hover:text-red-400 transition">
                üóëÔ∏è
              </button>
            </td>
          </tr>
        </tbody>
      </table>
    </div>

    <!-- Paginaci√≥n mejorada -->
    <div class="flex justify-center mt-6 space-x-2">
      <button @click="prevPage" :disabled="pagina === 1" class="px-4 py-2 border rounded-lg shadow-sm bg-gray-100 hover:bg-gray-200 disabled:opacity-50">
        ‚óÄ
      </button>
      <span class="px-4 py-2 text-gray-700">{{ pagina }} / {{ totalPaginas }}</span>
      <button @click="nextPage" :disabled="pagina >= totalPaginas" class="px-4 py-2 border rounded-lg shadow-sm bg-gray-100 hover:bg-gray-200 disabled:opacity-50">
        ‚ñ∂
      </button>
    </div>
  </div>
</template>


<script setup >
import { ref, computed, onMounted } from "vue";
import ProductosCrear from "./crear.vue";
import agregar from '../../assets/iconos/agregar.svg'
import buscar from '../../assets/iconos/buscar.svg'
import cubiertos from '../../assets/iconos/cubiertos.svg'


const productos = ref([]);
const searchQuery = ref("");
const mostrarModal = ref(false);
const pagina = ref(1);
const itemsPorPagina = 5;

onMounted(async () => {
  try {
    productos.value = await window.api.getProductos();
  } catch (err) {
    console.error("Error al cargar los productos", err);
  }
});

const paginatedProductos = computed(() => {
  const inicio = (pagina.value - 1) * itemsPorPagina;
  return productos.value.slice(inicio, inicio + itemsPorPagina);
});

const totalPaginas = computed(() => Math.ceil(productos.value.length / itemsPorPagina));
const nextPage = () => { if (pagina.value < totalPaginas.value) pagina.value++; };
const prevPage = () => { if (pagina.value > 1) pagina.value--; };

const abrirModal = () => { mostrarModal.value = true; };
const cerrarModal = () => { mostrarModal.value = false; };

const getStockClass = (producto) => {
  if (!producto.maneja_stock) return "bg-gray-300 text-gray-700 px-2 py-1 rounded";
  if (producto.stock <= 0) return "bg-red-500 text-white px-2 py-1 rounded";
  return "bg-red-500 text-white px-2 py-1 rounded";
};
</script> 